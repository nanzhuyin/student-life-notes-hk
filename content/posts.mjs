- 存殁：在世选“存”，已故选“殁/已故”。
- 姓名：按家人身份证件或户籍资料填写正体中文。
- 出生日期：按西元日期填写，不能只写年龄。
- 性别：页面出现该字段时按事实选择。
- 职业：在职填真实职业；退休可填“退休”，无业只能在事实相符时填写。
- 现住址：填写家人目前真实居住地址，尽量到门牌号。
